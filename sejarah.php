                    berpegang teguh pada Panca Prasetya KORPRI PP Nomor 12 tentang Perubahan atas PP Nomor 5 Tahun 1999
                    muncul untuk mengatur keberadaan PNS yang ingin jadi anggota Parpol.</p>
        
                <p>Dengan adanya ketentuan di dalam PP ini membuat anggota KORPRI tidak dimungkinkan untuk ikut dalam
