                    dari beberapa pengurus dengan kondisi tersebut, sebaiknya KORPRI dibubarkan saja, atau bahkan jika
                    ingin berkiprah di kancah politik maka sebaiknya membentuk partai sendiri. Setelah Reformasi dengan
                    demikian KORPRI bertekad untuk netral dan tidak lagi menjadi alat politik.</p>
        
