                    muncul berbagai upaya agar pegawai negeri netral dari kekuasaan partai-partai yang berkuasa.</p>
        
                <p>Melalui Undang-Undang Nomor : 18 Tahun 1961 ditetapkan bahwa … Bagi suatu golongan pegawai dan/atau
