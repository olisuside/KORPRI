                <p>Undang-Undang No 5 Tahun 2014 tentang Aparatur Sipil Negara (ASN) segera diberlakukan. Sebagai
                    implementasi nya, Korps Pegawai Republik Indonesia atau yang lebih dikenal dengan singkatan KORPRI
                    ke depan harus lebih baik dan tetap eksis. Hal itu bisa dilakukan dengan cara penguatan kelembagaan.
                    Karena dengan penguatan kelembagaan, maka KORPRI akan semakin memahami tugas pokok dan fungsinya
                    sebagai lembaga non kedinasan yang anggotanya mayoritas Pegawai Negeri Sipil (PNS) yang bertugas
                    memberikan pelayanan prima kepada anggota masyarakat berdasarkan Keputusan Presiden nomor 82 tahun
                    1971 tentang KORPRI.</p>
        
                <p>Tujuan utama adanya UU ASN adalah meningkatnya independensi dan netralitas, kompetensi, kinerja,
                    integritas, kesejahteraan, kualitas pelayanan publik, serta meningkatnya pengawasan dan
                    akuntabilitas. Atas dasar itulah, maka ada beberapa perubahan secara prinsip yang harus dipahami
                    oleh aparatur utamanya pengurus KORPRI. Ini penting, karena ada beberapa ketentuan umum yang
                    prinsip, yaitu adanya status pegawai yang berubah menjadi ASN yang terdiri dari dua jenis yaitu
                    Pegawai Negeri Sipil (PNS) dan Pegawai Pemerintah dengan Perjanjian Kontrak (PPPK) yang bekerja di
                    instansi pemerintah.</p>
        
                <p>Sesuai Pasal 1 butir 3 dan Pasal 7 UU ASN, dijelaskan PNS adalah pegawai tetap dan memiliki NIP
                    secara nasional serta menduduki jabatan pemerintah. Mereka memperoleh hak gaji, tunjangan,
                    fasilitas, cuti, jaminan pensiun, jaminan hari tua, perlindungan dan pengembangan potensi. Sedangkan
                    Pasal 1 butir 4 dan Pasal 7 UU ASN dijelaskan PPPK adalah pegawai yang diangkat dengan perjanjian
                    kerja sesuai kebutuhan instansi dan ketentuan UU untuk melaksanakan tugas pemerintahan. Mereka
                    memperoleh hak gaji, tunjangan, fasilitas, cuti, perlindungan dan pengembangan potensi. Jadi pada
                    prinsipnya PNS dan PPPK memiliki hak yang sama. Yang membedakan hanya PPPK tidak dapat pensiun dan
                    masa kerjanya selama 1 tahun tapi dapat diperpanjang lagi. Selain itu, sesuai UU ASN juga
                    menjelaskan tentang kedudukan yang sama antara PNS dan PPPK. Keduanya memiliki kedudukan sebagai
                    unsur aparatur Negara, melaksanakan kebijakan yang ditetapkan oleh pimpinan, serta harus bebas dari
                    pengaruh/intervensi golongan dan partai politik. Sementara fungsi dan perannya sebagai pelaksana
                    kebijakan publik, pelayan publik, dan perekat serta pemersatu bangsa.</p>
        
                <p>KORPRI sebagai lembaga non kedinasan mempunyai tupoksi yang penting dan strategis bukan hanya
                    kepentingan anggotanya melainkan juga kepada masyarakat umum. Anggota KORPRI harus dapat memahami
                    makna yang terkandung dalam Panca Prasetya KORPRI agar dapat membangun kesadaran untuk memiliki
                    solidaritas dan soliditas yang kuat, menjalankan kode etik PNS serta menjalin persatuan dan
                    kesatuan. Kemudian juga sebagai perekat dalam membangun bangsa dan negara dengan terciptanya pegawai
                    yang professional, netral, produktif dan akuntabel, sehingga kinerja, kedispilinan dan kreativitas
                    PNS semakin meningkat dan kondisi pelayanan publik yang diharapkan masyarakat terwujud dimasa
                    mendatang.</p>
        
                <p>Dengan adanya KORPRI diharapkan dapat memberikan pengaruh positif bagi PNS agar dapat lebih
                    mengoptimalkan kinerjanya semakin baik di era Otonomi Daerah ini. Dengan pembinaan jiwa korsa akan
                    tertanam pada kepribadian PNS yang mempunyai kepekaan, kemandirian dan kerjasama yang baik.</p>
        
                <p>Dalam hal peningkatan pelayanan kepada masyarakat, KORPRI harus mengutamakan kepentingan negara
                    diatas kepentingan pribadi dan golongan sesuai dengan Panca Prasetya KORPRI butir ke-3. Jadi disini
                    jelas bahwa KORPRI harus netral dan terlepas dari semua kepentingan partai politik manapun. Dengan
                    demikian pelayanan kepada masyarakat akan sama dan optimal.</p>
        
                <p>KORPRI secara konsisten harus mampu mengimplementasikan kebijakan-kebijakan yang telah ditetapkan
                    pemerintah dalam dalam upaya mensukseskan pembangunan nasional. Tentu saja aturan-aturan yang ada
                    dalam lembaga KORPRI tidak boleh bertentangan dengan UU ASN yang telah disahkan awal tahun 2014
                    lalu. Ke depan KORPRI harus menjadi organisasi profesi yang professional, netral dari semua partai
                    politik dan menjadi wadah bagi perjuangan meningkatkan kesejahteraan anggotanya.</p>
        
                <p>Kelancaran tugas umum pemerintahan dan pembangunan nasional sangat dipengaruhi oleh kesempurnaan
                    pengabdian aparatur Negara. Untuk menjamin tercapainya tujuan pembangunan nasional diperlukan
                    pegawai yang netral, mampu menjaga persatuan dan kesatuan bangsa, professional dan bertanggungjawab
                    dalam melaksanakan tugas. Sebagai upaya mewujudkan Pegawai Negeri Sipil yang kuat, kompak dan
                    bersatu padu, memiliki kepekaan, tanggap dan memiliki kesetiakawanan yang tinggi, berdisiplin, serta
