                    Pegawai RI yang berada di daerah yang diduduki Belanda (Non Kolaborator) dan ketiga, pegawai
                    pemerintah yang bersedia bekerjasama dengan Belanda (Kolaborator).</p>
        
                <p>Setelah pengakuan kedaulatan RI tanggal 27 Desember 1949, seluruh pegawai RI, pegawai RI non
                    Kolaborator, dan pegawai pemerintah Belanda dijadikan Pegawai RI Serikat.
        
                <p>Era RIS, atau yang lebih dikenal dengan era pemerintahan parlementer diwarnai oleh jatuh bangunnya
                    kabinet. Sistem ketatanegaraan menganut sistem multi partai. Para politisi, tokoh partai mengganti
                    dan memegang kendali pemerintahan, hingga memimpin berbagai departemen yang sekaligus menyeleksi
                    pegawai negeri. Sehingga warna departemen sangat ditentukan oleh partai yang berkuasa saat itu.
                    Dominasi partai dalam pemerintahan terbukti mengganggu pelayanan publik.</p>
        
                <p>PNS yang seharusnya berfungsi melayani masyarakat (publik) dan negara menjadi alat politik partai.
