                <p>Pada awal era Orde Baru dilaksanakan penataan kembali pegawai negeri dengan munculnya Keppres RI
                    Nomor : 82 Tahun 1971 tentang KORPRI. Berdasarkan Kepres yang bertanggal 29 November 1971 itu,
                    KORPRI “merupakan satu-satunya wadah untuk menghimpun dan membina seluruh pegawai RI di luar
                    kedinasan” (Pasal 2 ayat 2). Tujuan pembentukannya Korps Pegawai ini adalah agar “Pegawai Negeri RI
