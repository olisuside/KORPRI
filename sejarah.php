                    Kondisi ini terus berlangsung hingga dikeluarkannya Dekrit Presiden 5 Juli 1959. Dengan Dekrit
                    Presiden ini sistem ketatanegaraan kembali ke sistem Presidensiil berdasar UUD 1945. Akan tetapi
                    dalam praktek kekuasaan Presiden sebagai kepala negara dan kepala pemerintahan sangatlah besar.</p>
        
                <p>Era ini lebih dikenal dengan masa Demokrasi Terpimpin, sistem politik dan sistem ketatanegaraan
