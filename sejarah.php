                <p>
                    Korps Pegawai Republik Indonesia atau biasa dikenal KORPRI berdiri berdasarkan Keputusan Presiden
                    Nomor : 82 Tahun 1971, 29 November 1971. Latar belakang sejarah KORPRI sendiri sangatlah panjang,
