                    Jepang secara otomatis dijadikan Pegawai Negara Kesatuan Republik Indonesia</p>
        
                <p>Pada tanggal 27 Desember 1949 Belanda mengakui kedaulatan RI, Pegawai NKRI terbagi menjadi tiga
