                    organisasi politik (pasal 10 ayat 3). Ketentuan tersebut diharapkan akan diperkuat dengan
                    dikeluarkan Peraturan Pemerintah (PP) yang mengaturnya, tetapi disayangkan bahwa, PP yang diharapkan
                    akan muncul ternyata tidak kunjung datang.</p>
        
                <p>Sistem pemerintahan demokrasi parlementer berakhir dengan meletusnya upaya kudeta oleh PKI dengan
