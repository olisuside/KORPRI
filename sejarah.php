                <p>Akan tetapi KORPRI kembali menjadi alat politik. UU No.3 Th.1975 tentang Partai Politik dan Golongan
                    Karya serta Peraturan Pemerintah No.20 Th.1976 tentang Keanggotaan PNS dalam Parpol, makin
                    memperkokoh fungsi KORPRI dalam memperkuat barisan partai. Sehingga setiap kali terjadi birokrasi
                    selalu memihak kepada salah satu partai, bahkan dalam setiap Musyawarah Nasional KORPRI, diputuskan
                    bahwa organisasi ini harus menyalurkan aspirasi politiknya ke partai tertentu.</p>
        
                <p>Memasuki Era reformasi muncul keberanian mempertanyakan konsep monoloyalitas KORPRI, sehinga sempat
